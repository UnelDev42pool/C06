#include <unistd.h>

void	print(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(int argc, char *argv[])
{
	while (argc > 1)
	{
		print(argv[argc - 1]);
		write(1, "\n", 1);
		argc--;
	}
}
